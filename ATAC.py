# CELL 12 — STEP 6A: TF-IDF Normalization
#
# TF-IDF is the standard normalization for ATAC-seq.
# TF  = count(cell,peak) / total_fragments(cell)  → library size correction
# IDF = log(1 + n_cells / n_cells_with_peak)       → peak frequency weighting
# Rare peaks (open in few cells) get upweighted — they carry cell-type info.
# Common peaks (open in all cells) get downweighted — they are uninformative.
#
# Raw counts preserved in layer "counts" for downstream reference.

import scipy.sparse as sp
import numpy as np

print("=" * 60)
print("STEP 6A — TF-IDF normalization")
print("=" * 60)

X = adata_atac.X
if not sp.issparse(X):
    X = sp.csr_matrix(X.astype(np.float64))
else:
    X = X.astype(np.float64)

n_cells, n_peaks = X.shape
print(f"  Input: {n_cells:,} cells × {n_peaks:,} peaks")

# Preserve raw counts
adata_atac.layers["counts"] = X.copy()
print("  Raw counts saved to layer 'counts'")

# TF: normalize each cell by its total fragment count
cell_totals = np.array(X.sum(axis=1)).flatten()
cell_totals[cell_totals == 0] = 1
TF = sp.diags(1e4 / cell_totals) @ X
print("  TF computed (scale=10,000)")

# IDF: penalize ubiquitous peaks, reward cell-type specific peaks
cells_per_peak = np.array((X > 0).sum(axis=0)).flatten()
IDF = np.log1p(n_cells / (cells_per_peak + 1))
print(f"  IDF computed (range: {IDF.min():.3f}–{IDF.max():.3f})")

# TF-IDF
adata_atac.X = TF.multiply(IDF).tocsr()
adata_atac.uns["normalization"] = "TF-IDF"
adata_atac.uns["scale_factor"]  = 1e4
print("  ✅ TF-IDF applied")

