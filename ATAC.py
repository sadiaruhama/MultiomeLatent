peak_files = {}

for sname, meta in SAMPLES.items():
    print(f"\n── {sname} ──")

    np_path = PEAKS_DIR / f"{sname}_peaks.narrowPeak"
    if np_path.exists():
        n = sum(1 for _ in open(np_path))
        print(f"  ✅ Already exists: {n:,} peaks — skipping")
        peak_files[sname] = str(np_path)
        continue

    frag_gz  = str(FRAG_DIR / meta["file"])
    barcodes = str(QC_DIR / f"{sname}_barcodes.txt")
    tmp_bed  = str(PEAKS_DIR / f"{sname}_filtered_fragments.bed")

    # Load QC-passed barcodes
    print("  Loading QC-passed barcodes...")
    with open(barcodes) as f:
        valid = set(line.strip() for line in f if line.strip())
    print(f"  Valid barcodes: {len(valid):,}")

    # Write filtered fragments as BED (3 columns only)
    print("  Writing filtered fragment intervals...")
    n_written = 0

    with gzip.open(frag_gz, "rt") as fin, open(tmp_bed, "w") as fout:
        for line in fin:
            if line.startswith("#"):
                continue

            parts = line.rstrip().split("\t")
            if len(parts) < 4:
                continue

            chrom = parts[0]
            start = parts[1]
            end   = parts[2]
            bc    = parts[3]

            if bc not in valid:
                continue

            fout.write(f"{chrom}\t{start}\t{end}\n")
            n_written += 1

    print(f"  Filtered fragment intervals written: {n_written:,}")

    # Run MACS3
    cmd = (
        f"macs3 callpeak "
        f"-t {tmp_bed} "
        f"-n {sname} "
        f"--outdir {str(PEAKS_DIR)} "
        f"-f BED "
        f"-g hs "
        f"--nomodel "
        f"--shift 0 "
        f"--extsize 200 "
        f"-q 0.01 "
        f"--keep-dup all "
        f"--call-summits"
    )

    print("  Running MACS3...")
    result = subprocess.run(cmd, shell=True, capture_output=True, text=True)

    if np_path.exists():
        n = sum(1 for _ in open(np_path))
        print(f"  ✅ {n:,} peaks called")
        peak_files[sname] = str(np_path)
    else:
        print("  ❌ MACS3 failed")
        print((result.stdout + "\n" + result.stderr)[-2000:])

    # Remove temp BED to save space
    if os.path.exists(tmp_bed):
        os.remove(tmp_bed)

print(f"\n✅ Step 2 complete — {len(peak_files)}/4 samples finished")
