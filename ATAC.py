import snapatac2 as snap
import pandas as pd
import numpy as np
import random
from pathlib import Path

QC_DIR = Path("/content/drive/MyDrive/New_BCG_ATAC_output/1_qc")

SAMPLES = {
    "h1_Pre_R1" : {"status": "Pre",  "donor": "R1", "gsm": "GSM8945136"},
    "h2_Post_R1": {"status": "Post", "donor": "R1", "gsm": "GSM8945137"},
    "h3_Pre_R2" : {"status": "Pre",  "donor": "R2", "gsm": "GSM8945138"},
    "h4_Post_R2": {"status": "Post", "donor": "R2", "gsm": "GSM8945139"},
}

# ── PUBLICATION THRESHOLDS — consistent for all samples ───────
TSS_MIN   = 4
FRAG_MIN  = 1000
FRAG_MAX  = 50000
MAX_CELLS = 10000   # downsample cap — balances paired design
SEED      = 42      # fixed seed for reproducibility

print("=" * 60)
print("STEP 1D — QC filtering (publication-standard)")
print("=" * 60)
print(f"  TSS ≥ {TSS_MIN}  |  Fragments {FRAG_MIN:,}–{FRAG_MAX:,}  |  Max {MAX_CELLS:,} cells")
print()

adatas_filtered = {}
qc_records = []

for sname, adata in adatas_raw.items():
    n_raw = adata.n_obs

    snap.pp.filter_cells(
        adata,
        min_counts = FRAG_MIN,
        max_counts = FRAG_MAX,
        min_tsse   = TSS_MIN,
    )
    n_qc = adata.n_obs

    # Make an in-memory copy after filtering, before potential downsampling
    # This mitigates potential issues with backed AnnData objects or type resolution
    adata = adata.copy()

    # Downsample to MAX_CELLS if needed
    if n_qc > MAX_CELLS:
        random.seed(SEED)
        keep_indices = random.sample(range(n_qc), MAX_CELLS)
        # Create a boolean mask for selection
        mask = np.zeros(n_qc, dtype=bool)
        mask[keep_indices] = True
        adata = adata[mask].copy()

    n_final = adata.n_obs
    adatas_filtered[sname] = adata

    qc_records.append({
        "sample"   : sname,
        "status"   : SAMPLES[sname]["status"],
        "donor"    : SAMPLES[sname]["donor"],
        "raw"      : n_raw,
        "after_qc" : n_qc,
        "final"    : n_final,
        "pct_kept" : round(100 * n_final / n_raw, 1),
    })

    ds_note = f" (downsampled from {n_qc:,})" if n_qc > MAX_CELLS else ""
    print(f"  {sname:15s}  {n_raw:6,} → {n_qc:6,} → {n_final:6,}{ds_note}")

qc_df = pd.DataFrame(qc_records)
qc_df.to_csv(str(QC_DIR / "QC_filtering_summary.csv"), index=False)

print(f"\n  Total cells after QC + downsampling: {qc_df['final'].sum():,}")
print("  (balanced ~10k per sample = publication-ready)")

# Save barcodes for MACS2
print("\n  Saving barcode lists...")
for sname, adata in adatas_filtered.items():
    bc_path = str(QC_DIR / f"{sname}_barcodes.txt")
    pd.Series(adata.obs_names.tolist()).to_csv(bc_path, index=False, header=False)
    print(f"    {sname}: {adata.n_obs:,} barcodes saved")

print("\n✅ Step 1D complete")

