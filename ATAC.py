    print(f"\n  ── {sname} ──")

    # Always ensure we are working with a pandas DataFrame for column checks and data extraction.
    # This avoids issues with PyDataFrameElem on backed AnnData objects.
    # The to_pandas() call creates a *copy* of the observation DataFrame as a pandas DataFrame.
    temp_obs_df = adata.obs[:].to_pandas()

    if "tsse" not in temp_obs_df.columns:
        # If 'tsse' is not in the columns, we need to compute it.
        # snap.metrics.tsse modifies adata.obs in-place, potentially back to PyDataFrameElem.
        snap.metrics.tsse(adata, gene_anno=snap.genome.hg38)
        # After computation, re-extract adata.obs as a pandas DataFrame for consistency.
        temp_obs_df = adata.obs[:].to_pandas()

    tsse_arr = np.array(temp_obs_df["tsse"])
    frag_arr = np.array(temp_obs_df["n_fragment"])

    valid    = ~np.isnan(tsse_arr) & ~np.isnan(frag_arr)
    tsse_arr = tsse_arr[valid]
    frag_arr = frag_arr[valid]

    real_cells = ((frag_arr > 1000) & (tsse_arr > 4)).sum()

    print(f"     Total barcodes     : {len(tsse_arr):,}")
    print(f"     Median TSS         : {np.median(tsse_arr):.2f}")
    print(f"     TSS > 4 (good)     : {(tsse_arr > 4).sum():,} ({(tsse_arr>4).mean()*100:.1f}%)")
