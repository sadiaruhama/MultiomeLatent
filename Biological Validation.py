# ── STEP 3E: Score peaks vs background ───────────────────────────────────────
# Approach:
# 1. Score top BCG peaks with each TF motif (PSSM threshold p < 0.0001)
# 2. Score shuffled background sequences
# 3. Fisher exact test: is the TF motif enriched in BCG peaks vs background?

import random
from Bio.Seq import Seq

random.seed(42)

# Load peak sequences
peak_records = list(SeqIO.parse(str(PEAK_FASTA), 'fasta'))
print(f'Peak sequences loaded: {len(peak_records)}')

# Create GC-content-matched background (shuffle within each sequence)
bg_records = []
for rec in peak_records:
    seq_list = list(str(rec.seq).upper())
    random.shuffle(seq_list)
    bg_rec = rec.__class__(seq=Seq(''.join(seq_list)), id=rec.id+'_bg', description='')
    bg_records.append(bg_rec)

def count_motif_hits(records, pssm, threshold_pct=0.85):
    """
    Count sequences containing at least one motif hit above threshold.
    threshold_pct: fraction of max PSSM score used as cutoff
    """
    max_score = pssm.max
    threshold = threshold_pct * max_score
    hits = 0
    for rec in records:
        seq_str = str(rec.seq).upper().replace('N', 'A')  # replace N with A
        if len(seq_str) < pssm.length:
            continue
        seq_obj = Seq(seq_str)
        # Score forward strand
        scores_fwd = pssm.calculate(seq_obj)
        scores_rev = pssm.calculate(seq_obj.reverse_complement())
        # Ensure array-like
        if hasattr(scores_fwd, '__iter__'):
            if max(scores_fwd) >= threshold or max(scores_rev) >= threshold:
                hits += 1
        else:
            if scores_fwd >= threshold or scores_rev >= threshold:
                hits += 1
    return hits

print('Scoring motifs (this may take a few minutes)...')

results = []
N_peaks = len(peak_records)
N_bg    = len(bg_records)

for tf_name, motif in tf_motifs.items():
    try:
        pssm = motif.pssm
        hits_peak = count_motif_hits(peak_records, pssm)
        hits_bg   = count_motif_hits(bg_records,   pssm)

        # Fisher exact test
        table = [[hits_peak,      N_peaks - hits_peak],
                 [hits_bg,        N_bg    - hits_bg  ]]
        odds, pval = ss.fisher_exact(table, alternative='greater')

        pct_peak = hits_peak / N_peaks * 100
        pct_bg   = hits_bg   / N_bg   * 100
        fold_enr = (pct_peak / pct_bg) if pct_bg > 0 else float('inf')

        results.append({
            'TF':             tf_name,
            'motif_id':       motif.matrix_id,
            'hits_BCG_peaks': hits_peak,
            'pct_BCG_peaks':  round(pct_peak, 1),
            'hits_background':hits_bg,
            'pct_background': round(pct_bg, 1),
            'fold_enrichment':round(fold_enr, 2),
            'odds_ratio':     round(odds, 3),
            'pval_fisher':    pval,
        })
        print(f'  {tf_name:8s}: peaks={pct_peak:.1f}%  bg={pct_bg:.1f}%  OR={odds:.2f}  p={pval:.3e}')
    except Exception as e:
        print(f'  {tf_name}: ERROR — {e}')

motif_df = pd.DataFrame(results)

