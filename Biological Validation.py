# ── STEP 3A: Extract peak sequences from MuData ───────────────────────────────
# This block handles the case where BCG_signature_peaks.bed is missing/empty
# by re-extracting peak coordinates from your MOFA results

import re
import muon as mu

def parse_peak_name(name):
    """Handle chr1:start-end and chr1-start-end formats."""
    m = re.match(r'(chr[\w]+):(\d+)-(\d+)', name)
    if m:
        return m.group(1), int(m.group(2)), int(m.group(3))
    m = re.match(r'(chr[\w]+)-(\d+)-(\d+)', name)
    if m:
        return m.group(1), int(m.group(2)), int(m.group(3))
    return None, None, None

