# CELL 4 — STEP 9A: Feature Selection for FULL DATASET MOFA+
#
# PURPOSE: Select informative features before MOFA+ training.
# Using all 22,826 genes and 107,748 peaks would be:
#   - Computationally prohibitive
#   - Dominated by noise features
#
# RNA: Select 3,000 highly variable genes (HVGs)
#   Standard for single-cell RNA analysis
#   Uses seurat_v3 method on lognorm layer
#
# ATAC: Select 50,000 highly variable peaks (HVPs)
#   Peaks that vary across cells capture cell-state differences
#   Constitutively open peaks carry no differential information
#
# PUBLICATION NOTE:
#   Feature selection is performed on the FULL dataset
#   (all cell types) to avoid bias from subsetting first.
#   HSPC-specific HVG selection is done separately in Cell 8.

