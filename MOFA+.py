# ── Signed scoring function ───────────────────────────────────
def score_signed(adata, up_genes, down_genes,
                 score_name="BCG_signed_score"):
    """
    Signed BCG signature score.
    Score = mean(up genes) - mean(down genes)
    Both measured in lognorm expression space.
    """
    import scipy.sparse as sp

    X = adata.layers["lognorm"] if "lognorm" in adata.layers else adata.X
    if sp.issparse(X):
        X = X.toarray()

    var_names = list(adata.var_names)

    up_idx   = [var_names.index(g) for g in up_genes
                if g in var_names]
    down_idx = [var_names.index(g) for g in down_genes
                if g in var_names]

    print(f"  Up genes found   : {len(up_idx)}/{len(up_genes)}")
    print(f"  Down genes found : {len(down_idx)}/{len(down_genes)}")

    up_score   = X[:, up_idx].mean(axis=1)   if up_idx   else np.zeros(X.shape[0])
    down_score = X[:, down_idx].mean(axis=1) if down_idx else np.zeros(X.shape[0])

    signed_score = up_score - down_score
    adata.obs[score_name] = signed_score

    return signed_score

