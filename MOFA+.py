"""#MOFA+"""

# CELL 9 — STEP 10B: Run MOFA+ on HSPC subset (MAIN ANALYSIS)
#
# This is the primary analysis for your paper.
#
# KEY PARAMETERS:
#   groups_label = "donor"
#     → MOFA+ treats R1 and R2 as separate groups
#     → Finds factors consistent in BOTH patients
#     → These consistent factors = true BCG effects (not patient-specific noise)
#
#   n_factors = 10
#     → Appropriate for 653 cells, 2 groups
#     → Avoids overfitting with small n
#
#   spikeslab_weights = True
#     → Sparse weights: each factor driven by small gene/peak subset
#     → Makes factors biologically interpretable
#     → Required for publication-quality factor analysis
#
#   center_groups = True
#     → Removes mean expression differences between patients
#     → Factors capture within-patient BCG changes, not patient differences
#
# EXPECTED FACTORS:
#   Factor 1-2: Cell cycle / proliferation (usually top factors in HSPCs)
#   Factor 3-5: BCG-associated chromatin/expression changes
#   Factor 6+:  Technical or minor biological variation

from mofapy2.run.entry_point import entry_point
import numpy as np
import scipy.sparse as sp
import pandas as pd
import warnings
warnings.filterwarnings("ignore")

print("=" * 60)
print("STEP 10B — MOFA+ training (HSPC subset — MAIN ANALYSIS)")
print(f"  {mdata_hspc.n_obs} HSPCs × RNA + ATAC")
print("=" * 60)

# ── Prepare RNA matrix ────────────────────────────────────────
print("\n  Preparing HSPC RNA matrix...")
rna_h = mdata_hspc["rna"].copy()
hvg_m = rna_h.var["highly_variable"].values

rna_X_h = rna_h.layers["lognorm"][:, hvg_m]
if sp.issparse(rna_X_h):
    rna_X_h = rna_X_h.toarray()
rna_X_h = rna_X_h.astype(np.float64)

rna_genes_h  = rna_h.var_names[hvg_m].tolist()
cell_names_h = rna_h.obs_names.tolist()
donor_h      = rna_h.obs["donor"].tolist()
donors_h     = sorted(set(donor_h))

print(f"  RNA: {rna_X_h.shape[0]} cells × {rna_X_h.shape[1]} HVGs")

# ── Prepare ATAC matrix ───────────────────────────────────────
print("  Preparing HSPC ATAC matrix...")
atac_h = mdata_hspc["atac"].copy()
hvp_m  = atac_h.var["highly_variable"].values

atac_X_h = atac_h.layers["lognorm"][:, hvp_m]
if sp.issparse(atac_X_h):
    atac_X_h = atac_X_h.toarray()
atac_X_h = atac_X_h.astype(np.float64)

atac_peaks_h = atac_h.var_names[hvp_m].tolist()
print(f"  ATAC: {atac_X_h.shape[0]} cells × {atac_X_h.shape[1]} HVPs")

# ── Split by donor ────────────────────────────────────────────
data_hspc = {"rna": {}, "atac": {}}
cell_names_by_donor = {}

for donor in donors_h:
    idx = [i for i, d in enumerate(donor_h) if d == donor]
    data_hspc["rna"][donor]  = rna_X_h[idx, :]
    data_hspc["atac"][donor] = atac_X_h[idx, :]
    cell_names_by_donor[donor] = [cell_names_h[i] for i in idx]
    print(f"  Group {donor}: {len(idx)} HSPCs")

# ── Initialize and train MOFA+ ────────────────────────────────
print("\n  Initializing MOFA+...")
ent_hspc = entry_point()

ent_hspc.set_data_options(
    scale_views   = False,
    center_groups = True,   # remove patient mean differences
)

ent_hspc.set_data_matrix(
    data = [[data_hspc["rna"][d]  for d in donors_h],
            [data_hspc["atac"][d] for d in donors_h]],
    views_names   = ["rna", "atac"],
    groups_names  = donors_h,
    features_names= [rna_genes_h, atac_peaks_h],
    samples_names = [cell_names_by_donor[d] for d in donors_h],
)

ent_hspc.set_model_options(
    factors          = N_FACTORS,
    spikeslab_weights= True,
    ard_factors      = True,
    ard_weights      = True,
)

ent_hspc.set_train_options(
    iter             = N_ITER,
    convergence_mode = "fast",
    seed             = SEED,
    verbose          = False,
    gpu_mode         = False,
)

print("  Training MOFA+ on HSPCs (5–15 minutes)...")
ent_hspc.build()
ent_hspc.run()

# ── Save model ────────────────────────────────────────────────
hspc_model_path = str(HSPC_DIR / "MOFA_HSPC.hdf5")
ent_hspc.save(hspc_model_path, save_data=True)
print(f"\n  ✅ HSPC model saved: {hspc_model_path}")
print("\n✅ Step 10B complete — HSPC MOFA+ trained")

