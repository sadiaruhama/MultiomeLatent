def prepare_mofa_matrices(mdata_subset):
    """Extract HVG/HVP-filtered RNA and ATAC matrices for MOFA+ input."""
    rna  = mdata_subset["rna"].copy()
    atac = mdata_subset["atac"].copy()

    hvg_m = np.asarray(mdata_hspc["rna"].var["highly_variable"]).astype(bool)
    hvp_m = np.asarray(mdata_hspc["atac"].var["highly_variable"]).astype(bool)

    rna_X = rna.layers["lognorm"][:, hvg_m]
    if sp.issparse(rna_X):
        rna_X = rna_X.toarray()
    rna_X = rna_X.astype(np.float64)

    atac_X = atac.layers["lognorm"][:, hvp_m]
    if sp.issparse(atac_X):
        atac_X = atac_X.toarray()
    atac_X = atac_X.astype(np.float64)

    gene_names = rna.var_names[hvg_m].tolist()
    peak_names = atac.var_names[hvp_m].tolist()
    cell_names = rna.obs_names.tolist()
    status     = rna.obs["status"].tolist()

    return rna_X, atac_X, gene_names, peak_names, cell_names, status


def train_single_donor_mofa(rna_X, atac_X, gene_names, peak_names,
                             cell_names, status, donor_label):
    """
    Train a single-group MOFA+ model on one donor's HSPCs.
    Returns (entry_point, factor_df) where factor_df has columns
    [Factor1..Factor10, status] indexed by cell barcode.
    """
    print(f"\n  Training MOFA+ on {donor_label} ({len(cell_names)} cells)...")

    ent = entry_point()

    ent.set_data_options(
        scale_views   = False,
        center_groups = False,   # single group — no centering needed
    )

    # Single group — pass as list-of-one
    ent.set_data_matrix(
        data          = [[rna_X], [atac_X]],
        views_names   = ["rna", "atac"],
        groups_names  = [donor_label],
        features_names= [gene_names, peak_names],
        samples_names = [cell_names],
    )

    ent.set_model_options(
        factors          = N_FACTORS_LOO,
        spikeslab_weights= True,
        ard_factors      = True,
        ard_weights      = True,
    )

    ent.set_train_options(
        iter             = N_ITER,
        convergence_mode = "fast",
        seed             = SEED,
        verbose          = False,
        gpu_mode         = False,
    )

    ent.build()
    ent.run()

    # Save model
    model_path = str(LOO_DIR / f"MOFA_LOO_{donor_label}.hdf5")
    ent.save(model_path, save_data=True)
    print(f"  Model saved: {model_path}")

    # Extract factor scores
    Z_raw = ent.model.nodes["Z"].getExpectation()
    if isinstance(Z_raw, dict):
        Z_arr = list(Z_raw.values())[0]
    elif isinstance(Z_raw, np.ndarray):
        Z_arr = Z_raw
    else:
        Z_arr = np.array(Z_raw)

    factor_names = [f"Factor{i+1}" for i in range(N_FACTORS_LOO)]
    factor_df    = pd.DataFrame(Z_arr, index=cell_names, columns=factor_names)
    factor_df["status"] = status
    factor_df["donor"]  = donor_label

    return ent, factor_df


def identify_bcg_factor(factor_df, donor_label):
    """
    Identify which factor correlates with BCG status (Post=1, Pre=0).
    Returns factor name and correlation results DataFrame.
    """
    status_num   = (np.array(factor_df["status"]) == "Post").astype(float)
    factor_names = [f"Factor{i+1}" for i in range(N_FACTORS_LOO)]

    records = []
    for fname in factor_names:
        f_scores = factor_df[fname].values
        r, p     = stats.pearsonr(f_scores, status_num)
        records.append({"factor": fname, "BCG_r": r, "BCG_p": p, "abs_BCG_r": abs(r)})

    factor_class = pd.DataFrame(records).sort_values("abs_BCG_r", ascending=False)

    print(f"\n  {donor_label} factor ~ BCG correlations:")
    for _, row in factor_class.head(5).iterrows():
        flag = "✅ BCG" if abs(row["BCG_r"]) > BCG_R_THRESHOLD else "   "
        print(f"    {row['factor']:<12} r={row['BCG_r']:+.4f}  p={row['BCG_p']:.4f}  {flag}")

    # Pick the factor with highest |BCG_r|
    best = factor_class.iloc[0]
    if best["abs_BCG_r"] < BCG_R_THRESHOLD:
        print(f"  ⚠️  No factor exceeds r={BCG_R_THRESHOLD} threshold in {donor_label}.")
        print(f"  Using {best['factor']} (best available: r={best['BCG_r']:+.4f})")
    else:
        print(f"  ✅ BCG factor identified: {best['factor']} (r={best['BCG_r']:+.4f})")

    return best["factor"], factor_class


def extract_weights(ent, bcg_factor_name, gene_names, peak_names, n_sig=N_SIGNATURE):
    """
    Extract RNA weights for the BCG factor from a trained model.
    Returns (rna_df_all, top_up_genes, top_down_genes).
    """
    W_all = ent.model.nodes["W"].getExpectation()

    if isinstance(W_all, dict):
        W_rna = list(W_all.values())[0]
    elif isinstance(W_all, (list, tuple)):
        W_rna = W_all[0]
    else:
        W_rna = W_all

    W_rna = np.asarray(W_rna)

    fi    = int(bcg_factor_name.replace("Factor", "")) - 1
    w_vec = W_rna[:, fi]

    rna_df = pd.DataFrame({
        "gene"      : gene_names,
        "weight"    : w_vec,
        "abs_weight": np.abs(w_vec),
        "direction" : ["up" if w > 0 else "down" for w in w_vec],
    }).sort_values("weight", ascending=False)

    top_up   = rna_df[rna_df["direction"] == "up"].head(n_sig)["gene"].tolist()
    top_down = rna_df[rna_df["direction"] == "down"].head(n_sig)["gene"].tolist()

    return rna_df, top_up, top_down

