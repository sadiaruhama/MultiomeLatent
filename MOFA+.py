def aucell_score(X_matrix, gene_names, signature_genes, top_n=TOP_N_WINDOW):
    genes_arr   = np.asarray(gene_names)
    sig_mask    = np.isin(genes_arr, signature_genes)
    n_found     = int(sig_mask.sum())
    if n_found < 5:
        return np.full(X_matrix.shape[0], np.nan), n_found
    sig_idx     = np.where(sig_mask)[0]
    sig_set     = set(sig_idx.tolist())
    top_n_use   = min(top_n, X_matrix.shape[1])
    scores      = np.zeros(X_matrix.shape[0], dtype=float)
    for i in range(X_matrix.shape[0]):
        ranked  = np.argsort(X_matrix[i, :])[::-1]
        top_idx = ranked[:top_n_use]
        scores[i] = np.isin(top_idx, list(sig_set)).sum() / n_found
    return scores, n_found

