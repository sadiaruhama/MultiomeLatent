# CELL X — Permutation test for Factor1 ~ BCG association
#
# PURPOSE:
#   Test whether observed correlation between a MOFA factor and BCG status
#   is stronger than expected by chance.
#
# IMPORTANT:
#   Shuffle labels WITHIN donor, not globally.
#   This preserves paired design structure (R1, R2).

import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
from scipy import stats
import warnings
warnings.filterwarnings("ignore")

FACTOR_NAME = "Factor1"   # change if needed
N_PERM = 1000
SEED = 42

print("=" * 60)
print(f"Permutation test — {FACTOR_NAME} ~ BCG status")
print("=" * 60)

# Extract scores and metadata
scores = mdata_hspc.obs[FACTOR_NAME].astype(float).values
status = (mdata_hspc.obs["status"] == "Post").astype(int).values   # Pre=0, Post=1
donor  = mdata_hspc.obs["donor"].astype(str).values

# Observed correlation
obs_r, obs_p = stats.pearsonr(scores, status)
print(f"Observed Pearson r = {obs_r:+.4f}")
print(f"Nominal Pearson p  = {obs_p:.6f}")

# Permutation: shuffle status within donor
rng = np.random.default_rng(SEED)
perm_r = np.zeros(N_PERM, dtype=float)

for i in range(N_PERM):
    status_perm = status.copy()

    for d in np.unique(donor):
        idx = np.where(donor == d)[0]
        status_perm[idx] = rng.permutation(status_perm[idx])

    perm_r[i], _ = stats.pearsonr(scores, status_perm)

# Two-sided empirical p-value
emp_p = (np.sum(np.abs(perm_r) >= abs(obs_r)) + 1) / (N_PERM + 1)

print(f"Empirical permutation p = {emp_p:.6f}")
print(f"95th percentile of |perm_r| = {np.percentile(np.abs(perm_r), 95):.4f}")

# Save results
perm_df = pd.DataFrame({
    "perm_r": perm_r
})
perm_df.to_csv(str(HSPC_DIR / f"{FACTOR_NAME}_permutation_distribution.csv"), index=False)

summary_df = pd.DataFrame([{
    "factor": FACTOR_NAME,
    "observed_r": obs_r,
    "observed_p_nominal": obs_p,
    "empirical_p": emp_p,
    "n_permutations": N_PERM
}])
summary_df.to_csv(str(HSPC_DIR / f"{FACTOR_NAME}_permutation_summary.csv"), index=False)

