# CELL 11 — BCG Factor Identification via Pearson Correlation
#
# METHOD (publication standard):
#   For each factor, compute:
#     BCG_r   = Pearson correlation of factor scores with BCG status
#               (Pre=0, Post=1)
#     donor_r = Pearson correlation of factor scores with donor
#               (R1=0, R2=1)
#
# CLASSIFICATION:
#   BCG factor   : |BCG_r| > 0.3  AND  |donor_r| < 0.5
#   Donor factor : |donor_r| > 0.3  AND  |BCG_r| < 0.3
#   Mixed        : both correlations high
#   Noise        : both correlations low
#
# WHY PEARSON NOT MANN-WHITNEY:
#   Pearson r gives a continuous effect size (0 to 1)
#   Directly reportable: "Factor 1 correlated with BCG (r=0.71)"
#   Allows ranking factors by strength of BCG association
#   Standard in MOFA+ publications

import numpy as np
import pandas as pd
from scipy import stats
import matplotlib.pyplot as plt
import seaborn as sns
import warnings
warnings.filterwarnings("ignore")

print("=" * 60)
print("BCG Factor Identification — Pearson Correlation")
print("=" * 60)

