# Main biological variable: BCG status
# Pre = 0, Post = 1
df["BCG_status"] = (df["status"] == "Post").astype(int)

# Add technical metrics
# Use already computed values if available
# If total_fragments/tss_enrichment are not in obs, use your tech_metrics dataframe
if "total_fragments" not in df.columns:
    df["total_fragments"] = tech_metrics["total_fragments"].values

if "tss_enrichment" not in df.columns:
    df["tss_enrichment"] = tech_metrics["tss_enrichment"].values

if "rna_total_counts" not in df.columns:
    if "total_counts" in mdata_hspc["rna"].obs.columns:
        df["rna_total_counts"] = mdata_hspc["rna"].obs["total_counts"].values
    else:
        df["rna_total_counts"] = tech_metrics["rna_total_counts"].values

if "n_genes" not in df.columns:
    if "n_genes_by_counts" in mdata_hspc["rna"].obs.columns:
        df["n_genes"] = mdata_hspc["rna"].obs["n_genes_by_counts"].values
    else:
        df["n_genes"] = tech_metrics["n_genes"].values

# Optional: log-transform depth-like variables
df["log_total_fragments"] = np.log1p(df["total_fragments"])
df["log_rna_total_counts"] = np.log1p(df["rna_total_counts"])

# Keep only needed columns and remove missing values
