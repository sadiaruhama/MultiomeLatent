    mo3  = sub[sub.obs["timepoint"] == "3mo"].obs["BCG_HSPC_score"].values
    u, p = stats.mannwhitneyu(mo3, pre, alternative="two-sided")
    rb   = 1 - (2*u) / (len(mo3)*len(pre))
    direction = "UP" if mo3.mean() > pre.mean() else "DOWN"
    print(f"  {donor}: pre mean={pre.mean():.4f}  3mo mean={mo3.mean():.4f}"
          f"  p={p:.4e}  rb={rb:+.3f}  [{direction}]")

