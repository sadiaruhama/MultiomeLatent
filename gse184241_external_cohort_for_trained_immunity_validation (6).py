# ── Statistical tests ─────────────────────────────────────────────────
print("\n" + "="*60)
print("STATISTICAL RESULTS — BCG-HSPC SIGNATURE SCORING")
print("="*60)

results = []

for stim in ["LPS", "RPMI"]:
    sub  = adata[adata.obs["stimulation"] == stim]
    pre  = sub[sub.obs["timepoint"] == "pre"].obs["BCG_HSPC_score"].values
    wk2  = sub[sub.obs["timepoint"] == "2wk"].obs["BCG_HSPC_score"].values
    mo3  = sub[sub.obs["timepoint"] == "3mo"].obs["BCG_HSPC_score"].values

    u1, p1 = stats.mannwhitneyu(wk2, pre, alternative="two-sided")
    u2, p2 = stats.mannwhitneyu(mo3, pre, alternative="two-sided")
    rb1 = 1 - (2*u1) / (len(wk2)*len(pre))
    rb2 = 1 - (2*u2) / (len(mo3)*len(pre))

    print(f"\nStimulation: {stim}")
    print(f"  Pre  : n={len(pre)},  mean={pre.mean():.4f}")
    print(f"  2wk  : n={len(wk2)}, mean={wk2.mean():.4f}")
    print(f"  3mo  : n={len(mo3)}, mean={mo3.mean():.4f}")
    print(f"  Pre vs 2wk : p={p1:.4e}  rank-biserial r={rb1:+.3f}")
    print(f"  Pre vs 3mo : p={p2:.4e}  rank-biserial r={rb2:+.3f}")

    results.append({"stimulation":stim,"comparison":"pre_vs_2wk",
                    "p":p1,"rank_biserial_r":rb1,
                    "pre_mean":pre.mean(),"post_mean":wk2.mean()})
    results.append({"stimulation":stim,"comparison":"pre_vs_3mo",
                    "p":p2,"rank_biserial_r":rb2,
                    "pre_mean":pre.mean(),"post_mean":mo3.mean()})

