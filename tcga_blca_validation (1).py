import pandas as pd
import numpy as np
import glob

print("\n" + "="*65)
print("STEP 1: Loading BCG-HSPC 100-gene signature")
print("="*65)

# Search for the signature file — handles .csv, .txt, .tsv variants
sig_patterns = [
    str(SIG_DIR / "BCG_signature_up_standard_100*"),
    str(SIG_DIR / "*up_standard_100*"),
    str(SIG_DIR / "*signature*up*100*"),
    str(SIG_DIR / "*BCG_signature_genes*"),
]

sig_file = None
for pattern in sig_patterns:
    matches = glob.glob(pattern)
    if matches:
        sig_file = matches[0]
        break

if sig_file is None:
    # List what IS in the hspc_analysis folder to help debug
    print("❌ Signature file not found. Files in hspc_analysis/:")
    for f in sorted(SIG_DIR.glob("*")):
        print(f"   {f.name}")
    raise FileNotFoundError(
        "Signature file not found. "
        "Check the filename in MyDrive/BCG_MOFA_output/hspc_analysis/"
    )

print(f"  ✅ Found: {os.path.basename(sig_file)}")

# ── Load the gene list ────────────────────────────────────────
ext = Path(sig_file).suffix.lower()

if ext in [".csv", ".tsv", ".txt"]:
    # Try reading as CSV/TSV first
    try:
        sig_df = pd.read_csv(sig_file, sep=None, engine="python")
        print(f"  Columns in file: {list(sig_df.columns)}")

        # Find the gene name column — common names
        gene_col_candidates = [
            c for c in sig_df.columns
            if any(k in c.lower() for k in ["gene", "name", "symbol", "hugo"])
        ]
        if gene_col_candidates:
            gene_col = gene_col_candidates[0]
        else:
            gene_col = sig_df.columns[0]   # fallback: first column

        print(f"  Using column: '{gene_col}'")
        bcg_genes = sig_df[gene_col].dropna().astype(str).str.strip().tolist()

    except Exception:
        # Plain text file — one gene per line
        with open(sig_file) as f:
            bcg_genes = [
                line.strip() for line in f
                if line.strip() and not line.startswith("#")
            ]

# Remove any empty/numeric strings
bcg_genes = [g for g in bcg_genes if g and not g.isdigit()]

print(f"\n  Total genes loaded : {len(bcg_genes)}")
print(f"  First 10 genes     : {bcg_genes[:10]}")
print(f"  Last  10 genes     : {bcg_genes[-10:]}")

if len(bcg_genes) < 10:
    raise ValueError(f"Only {len(bcg_genes)} genes loaded — check the file format")

# ─────────────────────────────────────────────────────────────
# CELL 3 — Load + clean expression matrix
# ─────────────────────────────────────────────────────────────
print("\n" + "="*65)
print("STEP 2: Loading + cleaning expression matrix")
print("="*65)

expr_raw = pd.read_csv(
    DATA_DIR / "data_mrna_seq_v2_rsem.txt",
    sep="\t", index_col=0, low_memory=False
)

# Drop Entrez ID column if present
if "Entrez_Gene_Id" in expr_raw.columns:
    expr_raw = expr_raw.drop(columns=["Entrez_Gene_Id"])

print(f"  Raw shape : {expr_raw.shape[0]:,} genes × {expr_raw.shape[1]:,} patients")

# ── 2a. Drop rows with missing gene names ─────────────────────
expr_raw = expr_raw[expr_raw.index.notna()]
expr_raw = expr_raw[expr_raw.index.astype(str).str.strip() != ""]
expr_raw = expr_raw[expr_raw.index.astype(str) != "nan"]
expr_raw.index = expr_raw.index.astype(str).str.strip()
print(f"  After dropping NaN gene names : {expr_raw.shape[0]:,} genes")

# ── 2b. Handle duplicate gene names (keep highest mean expression) ──
expr_raw = expr_raw.astype(float)
expr_raw = expr_raw.groupby(expr_raw.index).mean()
print(f"  After deduplication           : {expr_raw.shape[0]:,} genes")

# ── 2c. Log2 transform ────────────────────────────────────────
# Values showed max ~39,000 → raw RSEM scale, needs log2(x+1)
vmax = expr_raw.values.max()
if vmax > 100:
    print(f"  Max value = {vmax:.1f} → applying log2(x+1) transform")
    expr_log2 = np.log2(expr_raw + 1)
else:
    print(f"  Max value = {vmax:.1f} → already log2-scale, no transform needed")
    expr_log2 = expr_raw

print(f"  After log2 transform: min={expr_log2.values.min():.2f}  "
      f"max={expr_log2.values.max():.2f}  "
      f"mean={expr_log2.values.mean():.2f}")

# ── 2d. Check signature gene overlap ─────────────────────────
sig_in_data  = [g for g in bcg_genes if g in expr_log2.index]
sig_missing  = [g for g in bcg_genes if g not in expr_log2.index]
pct_overlap  = 100 * len(sig_in_data) / len(bcg_genes)

print(f"\n  Signature genes found in matrix : {len(sig_in_data)}/{len(bcg_genes)} ({pct_overlap:.1f}%)")
if sig_missing:
    print(f"  Missing genes                   : {sig_missing[:20]}")
    if len(sig_missing) > 20:
        print(f"  ... and {len(sig_missing)-20} more")

if pct_overlap < 50:
    print("  ⚠️  Less than 50% overlap — check gene symbol format in signature file")
elif pct_overlap >= 80:
    print("  ✅ Good overlap — ssGSEA will be reliable")

# Save cleaned matrix
expr_log2.to_csv(OUT_DIR / "expr_log2_clean.csv.gz", compression="gzip")
print(f"\n  ✅ Clean log2 matrix saved → results/expr_log2_clean.csv.gz")

# ─────────────────────────────────────────────────────────────
# CELL 4 — ssGSEA scoring
# ─────────────────────────────────────────────────────────────
print("\n" + "="*65)
print("STEP 3: ssGSEA scoring")
print("="*65)

import gseapy as gp

# ssGSEA expects: gene_sets = dict of {name: [genes]}
gene_sets = {"BCG_HSPC_Signature": sig_in_data}

print(f"  Running ssGSEA on {expr_log2.shape[1]} patients × {len(sig_in_data)} signature genes...")
print(f"  (This takes ~2-5 minutes)")

ss = gp.ssgsea(
    data        = expr_log2,          # genes × samples
    gene_sets   = gene_sets,
    outdir      = None,               # don't write intermediate files
    sample_norm_method = "rank",      # standard for ssGSEA
    no_plot     = True,
    processes   = 4,
    seed        = 42,
)

# Extract scores — result is samples × gene_sets
scores_df = ss.res2d

# Pivot to get one row per patient, one column = BCG_HSPC_Signature score
if "Term" in scores_df.columns and "NES" in scores_df.columns:
    # gseapy ≥0.10 format
    scores_pivot = scores_df.pivot(index="Name", columns="Term", values="NES")
elif "es" in scores_df.columns.str.lower().tolist():
    scores_pivot = scores_df
else:
    # Fallback: transpose if needed
    scores_pivot = scores_df.T

scores_pivot.index.name = "SAMPLE_ID"
scores_pivot.columns.name = None

# Rename score column for clarity
if "BCG_HSPC_Signature" in scores_pivot.columns:
    scores_pivot = scores_pivot.rename(columns={"BCG_HSPC_Signature": "BCG_score"})
else:
    scores_pivot.columns = ["BCG_score"]

print(f"\n  ssGSEA complete!")
print(f"  Score shape : {scores_pivot.shape}")
print(f"  Score range : min={scores_pivot['BCG_score'].min():.4f}  "
      f"max={scores_pivot['BCG_score'].max():.4f}  "
      f"mean={scores_pivot['BCG_score'].mean():.4f}")

scores_pivot.to_csv(OUT_DIR / "bcg_ssgsea_scores.csv")
print(f"  ✅ Scores saved → results/bcg_ssgsea_scores.csv")

