# ─────────────────────────────────────────────────────────────
# CELL 7 — Kaplan-Meier: Overall Survival
# ─────────────────────────────────────────────────────────────
print("\n" + "="*65)
print("STEP 5a: Kaplan-Meier — Overall Survival (OS)")
print("="*65)

from lifelines import KaplanMeierFitter
from lifelines.statistics import logrank_test
from lifelines.plotting import add_at_risk_counts

fig, ax = plt.subplots(figsize=(8, 6))

kmf_high = KaplanMeierFitter(label="BCG-High")
kmf_low  = KaplanMeierFitter(label="BCG-Low")

high = merged[merged["BCG_GROUP"] == "High"].dropna(subset=["OS_MONTHS","OS_EVENT"])
low  = merged[merged["BCG_GROUP"] == "Low"].dropna(subset=["OS_MONTHS","OS_EVENT"])

kmf_high.fit(high["OS_MONTHS"], high["OS_EVENT"])
kmf_low.fit( low["OS_MONTHS"],  low["OS_EVENT"])

kmf_high.plot_survival_function(ax=ax, color="#E74C3C", linewidth=2.5, ci_show=True)
kmf_low.plot_survival_function( ax=ax, color="#3498DB", linewidth=2.5, ci_show=True)

# Log-rank test
lr_os = logrank_test(
    high["OS_MONTHS"], low["OS_MONTHS"],
    event_observed_A=high["OS_EVENT"],
    event_observed_B=low["OS_EVENT"]
)

# Median survival
med_high = kmf_high.median_survival_time_
med_low  = kmf_low.median_survival_time_

ax.set_xlabel("Time (months)", fontsize=12)
ax.set_ylabel("Overall Survival Probability", fontsize=12)
ax.set_title("BCG-HSPC Signature — Overall Survival\nTCGA-BLCA (n=407)", fontsize=12)
ax.set_ylim(0, 1.05)

# Annotate p-value
pval_text = f"Log-rank p = {lr_os.p_value:.4f}"
if lr_os.p_value < 0.001:
    pval_text = f"Log-rank p < 0.001"
ax.text(0.65, 0.85, pval_text, transform=ax.transAxes,
        fontsize=11, fontweight="bold",
        bbox=dict(boxstyle="round,pad=0.3", facecolor="lightyellow", edgecolor="gray"))
ax.text(0.65, 0.77, f"Median OS  High: {med_high:.1f} mo", transform=ax.transAxes, fontsize=9, color="#E74C3C")
ax.text(0.65, 0.71, f"Median OS  Low:  {med_low:.1f} mo",  transform=ax.transAxes, fontsize=9, color="#3498DB")

add_at_risk_counts(kmf_high, kmf_low, ax=ax, fontsize=9)
ax.legend(fontsize=11)
plt.tight_layout()
fig.savefig(OUT_DIR / "02_KM_overall_survival.png", dpi=150, bbox_inches="tight")
plt.close()

print(f"  Log-rank p-value  : {lr_os.p_value:.6f}")
print(f"  Test statistic    : {lr_os.test_statistic:.4f}")
print(f"  Median OS (High)  : {med_high:.1f} months")
